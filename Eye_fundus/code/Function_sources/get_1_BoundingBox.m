%
%-%-%-%-%-%-%-%-%-%-%-%-%-%-%-%-%-%-%-%-%INPUTS%-%-%-%-%-%-%-%-%-%-%-%-%-%-%-%-%-%-%-%-%-%-%-%-%
%
% msk_ini        # mask
%
%-%-%-%-%-%-%-%-%-%-%-%-%-%-%-%-%-%-%-%-%OUTPUTS%-%-%-%-%-%-%-%-%-%-%-%-%-%-%-%-%-%-%-%-%-%-%-%-%
%
% BB            # bounding box : [ x_min , x_max , y_min , y_max ]
%
%-%-%-%-%-%-%-%-%-%-%-%-%-%-%-%-%-%-%-%-%EXAMPLE%-%-%-%-%-%-%-%-%-%-%-%-%-%-%-%-%-%-%-%-%-%-%-%-%
%
% im( BB(3):BB(4) , BB(1):BB(2) );
%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%
% REMARK : the box is around all the objects
%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% get_1_BoundingBox.m
% Guillaume NOYEL 12-09-2021
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%


function [BB] = get_1_BoundingBox( msk_ini )

msk = (msk_ini > 0);

s_h = any(msk); s_v = any(msk,2);
x_min = find(s_h,1,'first');
x_max = find(s_h,1,'last');
y_min = find(s_v,1,'first');
y_max = find(s_v,1,'last');

BB = [x_min,x_max,y_min,y_max];


